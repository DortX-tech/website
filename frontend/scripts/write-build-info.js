const { execSync } = require("child_process");
const fs = require("fs");
const path = require("path");

const frontendRoot = path.resolve(__dirname, "..");
const repoRoot = path.resolve(frontendRoot, "..");

function getBuildId() {
  if (process.env.REACT_APP_BUILD_ID) return process.env.REACT_APP_BUILD_ID.trim();
  try {
    return execSync("git rev-parse --short HEAD", {
      cwd: repoRoot,
      encoding: "utf8",
      stdio: ["ignore", "pipe", "ignore"],
    }).trim();
  } catch {
    return "unknown";
  }
}

const buildId = getBuildId();
const generatedAt = new Date().toISOString();
const envPath = path.join(frontendRoot, ".env.production.local");
const publicInfoPath = path.join(frontendRoot, "public", "build-info.json");

fs.writeFileSync(
  envPath,
  [
    "# Generated by scripts/write-build-info.js during npm run build.",
    `REACT_APP_BUILD_ID=${buildId}`,
    `REACT_APP_BUILD_TIME=${generatedAt}`,
    "",
  ].join("\n")
);

fs.writeFileSync(
  publicInfoPath,
  `${JSON.stringify({ buildId, generatedAt }, null, 2)}\n`
);

console.log(`Build marker generated: ${buildId}`);
